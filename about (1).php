It can also be used as an application for the Placement
Officers in the college to manage the student
information about placement thus reducing the manual
work and consumes less paperwork. The system also
provides the facility of viewing the personal and
academic information of the student. The system gets
the requested list of candidates for the companies who
would like to recruit the people according to their
eligibility criteria. Laravel framework is used to
facilitate the process of making the program.</div>
